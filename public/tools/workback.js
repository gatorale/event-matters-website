// DATE UTILITIES — all working-day calculations honour weekends + holidays
// =====================================================================

function makeHolidaySet(regions) {
  const set = new Set();
  for (const region of regions) {
    const list = HOLIDAY_CALENDARS[region] || [];
    for (const d of list) set.add(d);
  }
  return set;
}

function ymd(d) { return d.toISOString().slice(0, 10); }

function isHoliday(d, holidaySet) {
  return holidaySet.has(ymd(d));
}

function isWorkingDay(d, holidaySet) {
  const day = d.getDay();
  if (day === 0 || day === 6) return false;
  return !isHoliday(d, holidaySet);
}

// Add N working days to a date. N can be negative.
function addWorkingDays(date, n, holidaySet) {
  const d = new Date(date);
  if (n === 0) return d;
  const dir = n > 0 ? 1 : -1;
  let remaining = Math.abs(n);
  while (remaining > 0) {
    d.setDate(d.getDate() + dir);
    if (isWorkingDay(d, holidaySet)) remaining--;
  }
  return d;
}

// Subtract N working days from a date.
function subtractWorkingDays(date, n, holidaySet) {
  return addWorkingDays(date, -n, holidaySet);
}

// Snap a date to the previous working day if it falls on weekend/holiday
function snapToWorkingDay(date, holidaySet) {
  const d = new Date(date);
  while (!isWorkingDay(d, holidaySet)) {
    d.setDate(d.getDate() - 1);
  }
  return d;
}

// Snap a date to a specific weekday (1=Mon..5=Fri), choosing nearest within ±3 days
function snapToWeekday(date, targetDow, holidaySet) {
  const d = new Date(date);
  let diff = targetDow - d.getDay();
  if (diff > 3) diff -= 7;
  if (diff < -3) diff += 7;
  d.setDate(d.getDate() + diff);
  // If snapped date is a holiday, push back to nearest working day
  return snapToWorkingDay(d, holidaySet);
}

// Count working days inclusive between start and end
function networkDays(start, end, holidaySet) {
  if (!start || !end) return 0;
  const s = new Date(start), e = new Date(end);
  if (s > e) return 0;
  let count = 0;
  const cur = new Date(s);
  while (cur <= e) {
    if (isWorkingDay(cur, holidaySet)) count++;
    cur.setDate(cur.getDate() + 1);
  }
  return count;
}

function fmtDate(d) { return d ? d.toISOString().slice(0, 10) : ''; }
function fmtDateLong(d) {
  if (!d) return '';
  return d.toLocaleDateString('en-US', { weekday: 'short', month: 'short', day: 'numeric', year: 'numeric' });
}

function getDefaultEventStart() {
  const d = new Date();
  d.setMonth(d.getMonth() + 12);
  while (d.getDay() === 0 || d.getDay() === 6) d.setDate(d.getDate() - 1);
  return d;
}

