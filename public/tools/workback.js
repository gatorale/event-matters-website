function generate() {
  const eventName = document.getElementById('eventName').value.trim() || 'Untitled Event';
  const eventStart = new Date(document.getElementById('eventStart').value + 'T00:00:00');
  const eventEnd = new Date(document.getElementById('eventEnd').value + 'T00:00:00');
  const marketingDay = parseInt(document.getElementById('marketingDay').value, 10);
  const speakerDay = parseInt(document.getElementById('speakerDay').value, 10);
  const runwayMode = document.getElementById('runwayMode').value; // 'ideal' or 'min'
  
  if (isNaN(eventStart.getTime()) || isNaN(eventEnd.getTime())) {
    alert('Please enter valid event start and end dates.');
    return;
  }
  if (eventEnd < eventStart) {
    alert('Event end date must be on or after event start date.');
    return;
  }
  
  // Selected holiday regions
  const regions = [];
  document.querySelectorAll('#holidayGrid input[type="checkbox"]:checked').forEach(cb => {
    regions.push(cb.dataset.region);
  });
  const holidaySet = makeHolidaySet(regions);
  
  // Selected milestones (from individual milestone toggles)
  const selectedRows = new Set();
  document.querySelectorAll('.milestone-list-row input[type="checkbox"]:checked').forEach(cb => {
    selectedRows.add(parseInt(cb.dataset.milestoneRow, 10));
  });
  
  if (selectedRows.size === 0) {
    alert('Please select at least one milestone or phase.');
    return;
  }
  
  // Preserve user's row-level state across re-generates
  const prevExcluded = lastGenerated && lastGenerated.excludedRows ? lastGenerated.excludedRows : new Set();
  const prevAcknowledged = lastGenerated && lastGenerated.acknowledgedRows ? lastGenerated.acknowledgedRows : new Set();
  const prevCustomDurations = lastGenerated && lastGenerated.customDurations ? lastGenerated.customDurations : new Map();
  
  // === CALCULATION ===
  // Each milestone has a "days out from event start" anchor (working days).
  // All durations: ideal (Standard) or minimum (Sprint).
  // Working days exclude weekends + selected holiday calendars.
  // 
  // Excel NETWORKDAYS is inclusive on both endpoints — so a milestone with
  // daysOut=254 starting on day D counts D itself as one of the 254.
  // To replicate that, we subtract (daysOut - 1) working days from event start.
  //
  // Announcement milestones snap to the marketing or speaker comms day.
  // Row 31 (event live days) honours user-input dates directly.
  
  const computed = {};
  const EVENT_LIVE_ROW = 31;
  
  for (const m of MILESTONES) {
    if (!selectedRows.has(m.row)) continue;
    
    let start, end, duration;
    
    if (m.row === EVENT_LIVE_ROW) {
      start = eventStart;
      end = eventEnd;
      duration = networkDays(start, end, holidaySet);
    } else {
      // Methodology default duration based on runway mode
      duration = runwayMode === 'min' ? m.minDuration : m.idealDuration;
      // Override with user's custom duration if they've edited this milestone
      // in the results table. Persisted across re-generates.
      if (prevCustomDurations.has(m.row)) {
        duration = prevCustomDurations.get(m.row);
      }
      if (m.daysOut > 0) {
        start = subtractWorkingDays(eventStart, m.daysOut - 1, holidaySet);
      } else if (m.daysOut < 0) {
        start = addWorkingDays(eventStart, Math.abs(m.daysOut) - 1, holidaySet);
      } else {
        start = new Date(eventStart);
      }
      
      if (m.announcementType === 'Marketing') {
        start = snapToWeekday(start, marketingDay, holidaySet);
      } else if (m.announcementType === 'Speaker') {
        start = snapToWeekday(start, speakerDay, holidaySet);
      }
      
      // End = start + duration working days (matches Excel WORKDAY.INTL semantics).
      // For 1-day milestones, end equals start (per user requirement).
      end = duration > 1 ? addWorkingDays(start, duration, holidaySet) : start;
    }
    
    computed[m.row] = { start, end, milestone: m, duration };
  }
  
  // Build display rows in order, grouped by phase
  const rows = [];
  let currentPhase = null;
  for (const m of MILESTONES) {
    if (!computed[m.row]) continue;
    if (m.phaseId !== currentPhase) {
      // Phase header row
      const phaseChildren = Object.values(computed).filter(x => x.milestone.phaseId === m.phaseId);
      const phaseStart = phaseChildren.length ? new Date(Math.min(...phaseChildren.map(x => x.start.getTime()))) : null;
      const phaseEnd = phaseChildren.length ? new Date(Math.max(...phaseChildren.map(x => x.end.getTime()))) : null;
      rows.push({ isPhase: true, phaseId: m.phaseId, phase: m.phase, start: phaseStart, end: phaseEnd });
      currentPhase = m.phaseId;
    }
    const c = computed[m.row];
    rows.push({ isPhase: false, milestone: m, start: c.start, end: c.end, duration: c.duration });
  }
  
  // Preserve user's row-level state in the new lastGenerated.
  // (prevExcluded, prevAcknowledged, prevCustomDurations were captured earlier)
  
  lastGenerated = {
    eventName, eventStart, eventEnd,
    marketingDay, speakerDay,
    runwayMode, regions,
    holidaySet,
    rows,
    excludedRows: prevExcluded,
    acknowledgedRows: prevAcknowledged,
    customDurations: prevCustomDurations
  };
  
  renderResults();
  document.getElementById('exportBtn').disabled = false;
  document.getElementById('resultsSection').scrollIntoView({ behavior: 'smooth', block: 'start' });
}

