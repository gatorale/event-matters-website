function buildEmailPayload(email) {
  const data = lastGenerated;
  const generatedAt = new Date().toISOString().slice(0, 10);
  const rows = [];

  for (const r of data.rows) {
    if (r.isPhase) {
      const phaseHasIncluded = data.rows.some(x =>
        !x.isPhase &&
        x.milestone.phaseId === r.phaseId &&
        !data.excludedRows.has(x.milestone.row)
      );
      if (!phaseHasIncluded) continue;
      rows.push({ isPhase: true, phase: r.phase,
        start: r.start ? fmtDate(r.start) : '',
        end: r.end ? fmtDate(r.end) : '' });
      continue;
    }
    const m = r.milestone;
    if (data.excludedRows.has(m.row)) continue;
    const weeksOut = r.start
      ? Math.floor((data.eventStart - r.start) / (1000 * 60 * 60 * 24 * 7))
      : '';
    rows.push({
      isPhase: false,
      phase: m.phase, name: m.name, owner: m.owner,
      stakeholders: m.stakeholders,
      start: r.start ? fmtDate(r.start) : '',
      end: r.end ? fmtDate(r.end) : '',
      duration: r.duration,
      weeksOut,
      acknowledged: data.acknowledgedRows.has(m.row) ? 'Yes' : '',
      description: m.description
    });
  }

  return {
    email,
    eventName: data.eventName,
    eventStart: fmtDate(data.eventStart),
    eventEnd: fmtDate(data.eventEnd),
    marketingDay: DAY_NAMES[data.marketingDay],
    speakerDay: DAY_NAMES[data.speakerDay],
    runwayMode: data.runwayMode,
    customDurationsCount: data.customDurations.size,
    regions: data.regions.map(r => REGION_LABELS[r]),
    generatedAt,
    rows
  };
}

