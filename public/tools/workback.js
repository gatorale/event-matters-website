async function submitEmail() {
  const emailInput = document.getElementById('emailInput');
  const email = emailInput.value.trim();
  const err = document.getElementById('emailError');
  if (!email || !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    err.textContent = 'Please enter a valid email address.';
    return;
  }
  err.textContent = '';

  const submitBtn = document.getElementById('emailSubmitBtn');
  submitBtn.disabled = true;
  showEmailSending(email);

  try {
    const payload = buildEmailPayload(email);
    const response = await fetch('/api/send-workback', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload)
    });

    if (!response.ok) {
      const errData = await response.json().catch(() => null);
      const msg = (errData && errData.error) || ('Server error (' + response.status + ')');
      throw new Error(msg);
    }

    showEmailSent(email);
  } catch (e) {
    showEmailError(e.message || 'Something went wrong. Please try again.');
    submitBtn.disabled = false;
  }
}

